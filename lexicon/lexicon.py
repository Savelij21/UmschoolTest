class QuestionsGroup:
    title: str
    callback_data: str
    questions: list[Question]

    def get_questions_group_in_text(self) -> str:
        text = self.title + '\n\n'
        i = 1
        for question in self.questions:
            text += f'<b>{i}. {question.question_text}</b>\n'
            i += 1
        return text


